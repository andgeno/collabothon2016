package de.comdirect.collabothon2016.depotengine.data;

public class PortfolioInformationFromServerTO {

	private String depotnr;
	private String kdnr;
	private String abrechnungskonto;
	public String getDepotnr() {
		return depotnr;
	}
	public void setDepotnr(String depotnr) {
		this.depotnr = depotnr;
	}
	public String getKdnr() {
		return kdnr;
	}
	public void setKdnr(String kdnr) {
		this.kdnr = kdnr;
	}
	public String getAbrechnungskonto() {
		return abrechnungskonto;
	}
	public void setAbrechnungskonto(String abrechnungskonto) {
		this.abrechnungskonto = abrechnungskonto;
	}
	
	
}
